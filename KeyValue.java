public class KeyValue{
	private int key;
	private int value;

	public KeyValue(){
		key = 0;
		value = 0;
	}

	public void setKey(int key){
		this.key = key;
	}
	public void setValue(int value){
		this.value = value;
	}

	public int getKey(){
		return this.key;
	}
	public int getValue(){
		return this.value;
	}
}